def load_wide():
    p = os.path.join(DATA_DIR, 'wide_table.csv')
    return pd.read_csv(p) if os.path.exists(p) else None

def D(): return Document()

def H(doc, text, level=1):
    h = doc.add_heading(text, level=level)
    return h

def P(doc, text, bold=False, size=11, color=None, align=None, indent=0):
    p = doc.add_paragraph()
    if indent: p.paragraph_format.left_indent = Cm(indent)
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.font.name = 'Microsoft YaHei'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), 'Microsoft YaHei')
    if bold: run.bold = True
    if color: run.font.color.rgb = RGBColor(*color)
    if align is not None: p.alignment = align
    return p

def T(doc, headers, rows):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Light Grid Accent 1'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    for j, h in enumerate(headers):
        cell = table.rows[0].cells[j]; cell.text = str(h)
        for p in cell.paragraphs:
            for r in p.runs: r.bold = True; r.font.size = Pt(9)
    for i, row in enumerate(rows):
        for j, val in enumerate(row):
            cell = table.rows[i+1].cells[j]
            cell.text = str(val) if val is not None else ''
    doc.add_paragraph()
    return table

def add_chart_ref(doc, chart_name, description):
    P(doc, f'[图表: {chart_name}] {description}', size=9, color=(0x6B,0x72,0x80), indent=0.5)

def add_insight(doc, text, type='info'):
    colors = {'info': (0x25,0x63,0xEB), 'warn': (0xF5,0x9E,0x0B), 'good': (0x10,0xB9,0x81), 'bad': (0xEF,0x44,0x44)}
    P(doc, f'> {text}', size=10, color=colors.get(type, (0x25,0x63,0xEB)), indent=0.5)

def cover_page(doc, week_num, week_title, subtitle=''):
    P(doc, '', size=30)
    P(doc, 'G9 智能销售运营决策系统', bold=True, size=30, color=(0x25,0x63,0xEB), align=1)
    P(doc, f'W{week_num}: {week_title}', bold=True, size=22, color=(0x1E,0x29,0x3B), align=1)
    if subtitle: P(doc, subtitle, size=14, color=(0x6B,0x72,0x80), align=1)
    P(doc, f'报告日期: {datetime.date.today().isoformat()}', size=11, align=1)
    P(doc, '机密 | 数据仅本地处理 | 不外传', size=9, color=(0x9C,0xA3,0xAF), align=1)
    doc.add_page_break()

