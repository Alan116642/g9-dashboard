    H(doc, '第一章  研究框架', 1)
    P(doc, 'W1建立了数据基础，W2的核心任务是从"相关性"上升到"因果性"。相关性只能告诉我们两个变量'
             '一起变化，但不能回答"如果改变X，Y会怎样"这个业务决策的核心问题。')
    P(doc, '本阶段采用四种互补的因果推断方法，构建完整的因果证据链：')
    P(doc, '1. PSM (倾向得分匹配): 消除选择偏差，估计跟进方式的因果效应')
    P(doc, '2. RDD (断点回归): 利用延迟交付的断点，识别对满意度的因果影响')
    P(doc, '3. IV (工具变量): 处理遗漏变量问题，提供补充识别策略')
    P(doc, '4. Shapley Value归因: 公平分配各渠道对转化的贡献')

    H(doc, '第二章  PSM分析 — 跟进方式的因果效应', 1)
    H(doc, '2.1 研究设计', 2)
    P(doc, '处理变量: 跟进方式(面谈=1, 微信/电话=0)')
    P(doc, '结果变量: 是否下订(0/1)')
    P(doc, '协变量: 客户年龄、性别、城市、渠道、试驾时长')
    P(doc, '倾向得分模型对比:')
    T(doc,
        ['模型', 'AUC', '特点'],
        [['Logistic回归', '~0.62', '可解释性强, 作为基线'],
         ['GradientBoosting', '~0.68', '非线性拟合好, 过拟合风险高'],
         ['RandomForest', '~0.65', '鲁棒性好, 方差小']])
    P(doc, '选择AUC最高的GradientBoosting作为倾向得分模型。')

    H(doc, '2.2 匹配过程', 2)
    P(doc, '采用最近邻匹配(1:1, caliper=0.2×SD)，关键参数：')
    P(doc, '• Caliper = 0.2 × SD(倾向得分) — 限制匹配距离，保证匹配质量')
    P(doc, '• 不放回匹配 — 每个对照样本最多匹配一次')
    P(doc, '• 匹配后检查平衡性: 所有协变量SMD < 0.1 视为合格')
    add_chart_ref(doc, 'w2_01_psm_propensity_dist.png', '匹配前后倾向得分分布对比')
    add_chart_ref(doc, 'w2_02_psm_love_plot.png', 'Love Plot: 匹配前后协变量SMD变化')

    H(doc, '2.3 因果效应估计', 2)
    P(doc, 'ATT (处理组平均处理效应) = E[Y(1)|T=1] - E[Y(0)|T=1]')
    P(doc, '也就是: 实际接受面谈跟进的客户，如果他们没有接受面谈，转化率会差多少。')
    P(doc, 'PSM分析结论: 面谈跟进对转化率有正向因果效应。该结论经过以下验证：')
    P(doc, '• 平衡性检验: 匹配后所有协变量SMD < 0.1')
    P(doc, '• Rosenbaum敏感性分析: 结论对中等程度的隐藏偏差稳健')
    P(doc, '• 多模型交叉验证: Logistic/RF/GBM三个模型方向一致')
    add_chart_ref(doc, 'w2_04_psm_att_forest.png', 'ATT因果效应森林图(含95%CI)')
    add_chart_ref(doc, 'w2_05_psm_sensitivity.png', 'Rosenbaum敏感性分析: Gamma vs p值')

    H(doc, '第三章  RDD分析 — 延迟交付的影响', 1)
    H(doc, '3.1 研究设计', 2)
    P(doc, '利用"是否延迟交付"作为断点，分析其对交付评分的因果效应。')
    P(doc, '• 断点变量: 是否延迟交付 (0=准时, 1=延迟)')
    P(doc, '• 结果变量: 交付评分 (2.0-5.0)')
    P(doc, '• 运行变量: 交付里程 (作为延迟风险的代理)')
    P(doc, '关键数据: 延迟交付率50.5%(1,554/3,076)，准时交付评分3.45 vs 延迟交付评分3.46。'
             '表面上差异很小，但RDD通过局部估计揭示了断点附近的真实处理效应。')

    H(doc, '3.2 稳健性检验', 2)
    P(doc, '为确保RDD结论的可信度，进行了以下检验：')
    P(doc, '1. McCrary检验: 运行变量密度在断点处连续 → 无操纵证据')
    P(doc, '2. Placebo断点检验: 在假断点处RDD效应不显著 → 断点选择合理')
    P(doc, '3. 带宽敏感性: 0.5×到2×最优带宽内效应方向一致 → 结论稳健')
